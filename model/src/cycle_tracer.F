C     !INPUT/OUTPUT PARAMETERS:
C     == Routine Arguments ==
C     bi,bj,iMin,iMax,jMin,jMax, K :: Loop counters
C     tracer, gTracer, gTrNm1      :: Tracer field, with tendencies
C     myThid                       :: Instance number for 
